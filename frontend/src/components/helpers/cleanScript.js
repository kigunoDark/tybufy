export function cleanScript(text) {
  return text
    .replace(/\*\*.+\*\*/g, '')            // Удалить **Заголовки**
    .replace(/###\s*.+/g, '')              // Удалить ### Вступление
    .replace(/$[^)]+$/g, '')               // Удалить (скобки с описаниями)
    .replace(/^\d+\.\s+.+/gm, '')          // Удалить пункты вроде 1. Пункт
    .replace(/\n\s*\n/g, '\n\n')           // Почистить лишние пустые строки
    .trim();
}